<?php

class  calculadora {
    function sumar ($numero1, $numero2, $numero3)
    {
        return $numero1 + $numero2 + $numero3;
    }

    function resta ($numero1, $numero2, $numero3)
    {
        return $numero1 - $numero2 - $numero3;
    }

    function multiplicacion ($numero1, $numero2, $numero3)
    {
        return $numero1 * $numero2 * $numero3;
    }

    function division ($numero1, $numero2, $numero3)
    {
        return $numero1 / $numero2 / $numero3;
    }
}